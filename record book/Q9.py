# Q9Write a program to create a list of 6 different elements & show following slicing operation
# i.	 to all elements
# ii.	2 to 4 elements 
# iii.	3rd to all element 
# iv.	iv . 4 to all elements

data=[10,12.6,"A","string",True]
print("All elements =",data)
print("2 to 4 elements =",data[1:4])
print("3 to All elements =",data[2:])
print("4 to All elements =",data[3:])
